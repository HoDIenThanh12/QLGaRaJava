    private boolean KiemTraSDT(String s){
        int i=s.length();
        if(i<10 || i>11)
            return true;
        for(int j=0;j<i;j++){
            if(s.charAt(j)>='a' && s.charAt(j)<='z' || s.charAt(j)>='A' && s.charAt(j)<='A'){
                return  true;
            }
        }
        return false;
    }
